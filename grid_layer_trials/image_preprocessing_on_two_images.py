def apply_clahe_bilateral(img, lbl):
    pbar(f'{lbl}: bgr -> lab', 1, 8)
    lab = cv2.cvtColor(img, cv2.COLOR_BGR2LAB)
    pbar(f'{lbl}: split l,a,b channels', 2, 8)
    l, a, b = cv2.split(lab)
    pbar(f'{lbl}: clahe object', 3, 8)
    clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8, 8))
    pbar(f'{lbl}: apply clahe', 4, 8)
    l_eq = clahe.apply(l)
    pbar(f'{lbl}: merge', 5, 8)
    clahe_out = cv2.cvtColor(cv2.merge([l_eq, a, b]), cv2.COLOR_LAB2BGR)
    pbar(f'{lbl}: bilateral filter', 6, 8)
    result = cv2.bilateralFilter(clahe_out, d=9, sigmaColor=75, sigmaSpace=75)
    pbar(f'{lbl}: packing result', 7, 8)
    pbar(f'{lbl}: done', 8, 8)
    return result

