def get_grid_kp(img):
    h, w = img.shape[:2]
    r = 4
    c = 4
    min_k = 15
    dh = h // r
    dw = w // c
    s1 = cv2.SIFT_create(contrastThreshold=0.04)
    s2 = cv2.SIFT_create(contrastThreshold=0.01)
    kps = []
    for i in range(r):
        for j in range(c):
            y1 = i * dh
            y2 = h if i == r - 1 else (i + 1) * dh
            x1 = j * dw
            x2 = w if j == c - 1 else (j + 1) * dw
            roi = img[y1:y2, x1:x2]
            k = s1.detect(roi, None)
            if len(k) < min_k:
                k = s2.detect(roi, None)
            for p in k:
                p.pt = (p.pt[0] + x1, p.pt[1] + y1)
                kps.append(p)
    return kps

